public class bitwiseoperator {
    public static void main(String[] args) {
        int x =5,y=3;
        int result = x&y;
        int result1 = x|y;
        int result2 = x^y;
        int result3 = ~y;
        System.out.println(result);
        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);
    }
}
